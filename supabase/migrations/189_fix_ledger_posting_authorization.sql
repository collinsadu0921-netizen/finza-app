-- ============================================================================
-- Add column with DEFAULT to let PostgreSQL automatically backfill existing rows
-- This avoids the immutability trigger issue since PostgreSQL backfills atomically
ALTER TABLE journal_entries
  ADD COLUMN IF NOT EXISTS posting_source TEXT 
    CHECK (posting_source IN ('system', 'accountant'))
    DEFAULT 'accountant';

-- If column already existed (from a previous failed migration), we need to handle NULLs
-- Temporarily disable the immutability trigger to allow UPDATE
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM journal_entries WHERE posting_source IS NULL
  ) THEN
    -- Temporarily disable trigger to allow UPDATE
    ALTER TABLE journal_entries DISABLE TRIGGER trigger_prevent_journal_entry_modification;
    
    UPDATE journal_entries
    SET posting_source = 'accountant'
    WHERE posting_source IS NULL;
    
    -- Re-enable trigger
    ALTER TABLE journal_entries ENABLE TRIGGER trigger_prevent_journal_entry_modification;
  END IF;
END $$;

