-- ============================================================================
-- STEP 7: Update apply_adjusting_journal() to use posting_source = 'accountant'
-- ============================================================================
-- This function calls post_journal_entry, which now defaults to 'accountant'
-- but we make it explicit for clarity
CREATE OR REPLACE FUNCTION apply_adjusting_journal(
  p_business_id UUID,
  p_period_start DATE,
  p_entry_date DATE,
  p_description TEXT,
  p_lines JSONB,
  p_created_by UUID,
  p_adjustment_reason TEXT,
  p_adjustment_ref TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_period RECORD;
  v_account RECORD;
  v_journal_entry_id UUID;
  v_line JSONB;
  v_total_debit NUMERIC := 0;
  v_total_credit NUMERIC := 0;
  v_account_id UUID;
  v_debit NUMERIC;
  v_credit NUMERIC;
  v_line_count INTEGER := 0;
  v_affected_accounts JSONB := '[]'::jsonb;
  v_account_code TEXT;
  v_account_name TEXT;
  v_period_id UUID;
BEGIN
  -- ========================================================================
  -- VALIDATION 1: Period exists and status allows adjustments
  -- ========================================================================
  SELECT * INTO v_period
  FROM accounting_periods
  WHERE business_id = p_business_id
    AND period_start = p_period_start;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Accounting period not found for period_start: %', p_period_start;
  END IF;

  v_period_id := v_period.id;

  -- PHASE 6: Allow adjustments in 'open' OR 'soft_closed' periods
  -- Block adjustments in 'locked' periods
  IF v_period.status = 'locked' THEN
    RAISE EXCEPTION 'Adjusting journals cannot be posted into locked periods. Period status: %.', v_period.status;
  END IF;

  IF v_period.status NOT IN ('open', 'soft_closed') THEN
    RAISE EXCEPTION 'Adjusting journals can only be posted into periods with status ''open'' or ''soft_closed''. Period status: %.', v_period.status;
  END IF;

  -- ========================================================================
  -- VALIDATION 2: entry_date must fall within period [period_start, period_end]
  -- ========================================================================
  IF p_entry_date < v_period.period_start OR p_entry_date > v_period.period_end THEN
    RAISE EXCEPTION 'Entry date % must fall within period [%, %]', p_entry_date, v_period.period_start, v_period.period_end;
  END IF;

  -- ========================================================================
  -- VALIDATION 3: adjustment_reason is required
  -- ========================================================================
  IF p_adjustment_reason IS NULL OR TRIM(p_adjustment_reason) = '' THEN
    RAISE EXCEPTION 'Adjustment reason is required and cannot be empty';
  END IF;

  -- ========================================================================
  -- VALIDATION 4: At least 2 lines required
  -- ========================================================================
  v_line_count := jsonb_array_length(p_lines);
  IF v_line_count < 2 THEN
    RAISE EXCEPTION 'Adjusting journal must have at least 2 lines. Found: %', v_line_count;
  END IF;

  -- ========================================================================
  -- VALIDATION 5: Validate accounts and compute totals
  -- ========================================================================
  FOR v_line IN SELECT * FROM jsonb_array_elements(p_lines)
  LOOP
    -- Validate required fields
    IF NOT (v_line ? 'account_id') THEN
      RAISE EXCEPTION 'Each line must have an account_id';
    END IF;

    v_account_id := (v_line->>'account_id')::UUID;

    -- Validate account exists and belongs to business
    SELECT * INTO v_account
    FROM accounts
    WHERE id = v_account_id
      AND business_id = p_business_id
      AND deleted_at IS NULL;

    IF NOT FOUND THEN
      RAISE EXCEPTION 'Account not found or does not belong to business: %', v_account_id;
    END IF;

    v_account_code := v_account.code;
    v_account_name := v_account.name;

    -- Validate amounts
    v_debit := COALESCE((v_line->>'debit')::NUMERIC, 0);
    v_credit := COALESCE((v_line->>'credit')::NUMERIC, 0);

    -- Exactly one of debit or credit must be > 0
    IF v_debit <= 0 AND v_credit <= 0 THEN
      RAISE EXCEPTION 'Each line must have either debit > 0 or credit > 0';
    END IF;

    IF v_debit > 0 AND v_credit > 0 THEN
      RAISE EXCEPTION 'Each line must have either debit OR credit, not both';
    END IF;

    -- Accumulate totals
    v_total_debit := v_total_debit + v_debit;
    v_total_credit := v_total_credit + v_credit;

    -- Track affected accounts
    v_affected_accounts := v_affected_accounts || jsonb_build_object(
      'account_code', v_account_code,
      'account_name', v_account_name,
      'debit', v_debit,
      'credit', v_credit
    );
  END LOOP;

  -- ========================================================================
  -- VALIDATION 6: Debit/credit totals must balance exactly
  -- ========================================================================
  IF ABS(v_total_debit - v_total_credit) > 0.01 THEN
    RAISE EXCEPTION 'Adjusting journal entry must balance. Debit: %, Credit: %, Difference: %', v_total_debit, v_total_credit, ABS(v_total_debit - v_total_credit);
  END IF;

  -- ========================================================================
  -- CREATE JOURNAL ENTRY (using enhanced post_journal_entry function)
  -- ========================================================================
  -- PHASE 6: Use adjustment-only path with proper metadata
  -- EXPLICIT: Use posting_source = 'accountant' for manual accountant postings
  SELECT post_journal_entry(
    p_business_id,
    p_entry_date,
    p_description,
    'adjustment',  -- reference_type marks this as adjustment
    NULL,  -- reference_id is NULL (adjustments are standalone entries)
    p_lines,
    TRUE,  -- is_adjustment = TRUE
    p_adjustment_reason,  -- adjustment_reason (required)
    p_adjustment_ref,  -- adjustment_ref (optional)
    p_created_by,  -- created_by
    NULL,  -- p_entry_type
    NULL,  -- p_backfill_reason
    NULL,  -- p_backfill_actor
    p_created_by,  -- p_posted_by_accountant_id (required for accountant postings)
    'accountant'  -- p_posting_source: Manual adjustments are accountant postings
  ) INTO v_journal_entry_id;

  -- ========================================================================
  -- PHASE 6: MANDATORY AUDIT LOGGING
  -- ========================================================================
  INSERT INTO accounting_adjustment_audit (
    business_id,
    period_id,
    journal_entry_id,
    actor_user_id,
    adjustment_reason,
    adjustment_ref,
    affected_accounts,
    total_debit,
    total_credit
  )
  VALUES (
    p_business_id,
    v_period_id,
    v_journal_entry_id,
    p_created_by,
    p_adjustment_reason,
    p_adjustment_ref,
    v_affected_accounts,
    v_total_debit,
    v_total_credit
  );

  -- ========================================================================
  -- RETURN JOURNAL ENTRY ID
  -- ========================================================================
  RETURN v_journal_entry_id;

EXCEPTION
  WHEN OTHERS THEN
    -- Re-raise with context
    RAISE EXCEPTION 'Failed to apply adjusting journal: %', SQLERRM;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION apply_adjusting_journal IS 
  'PHASE 6: Atomically validates and applies adjusting journal entry. Uses posting_source = ''accountant'' for manual accountant postings. Enforces period status = ''open'' or ''soft_closed'' (not locked), entry_date within period, account validation, balanced entry, minimum 2 lines, and required adjustment_reason. Creates new journal entry marked with is_adjustment=TRUE and reference_type=''adjustment''. Logs to accounting_adjustment_audit table. Adjustments are permanent and auditable.';

-- ============================================================================
-- STEP 8: Update post_adjustment_to_ledger() to set posting_source = 'accountant'
-- ============================================================================
CREATE OR REPLACE FUNCTION post_adjustment_to_ledger(
  p_business_id UUID,
  p_adjustment_date DATE,
  p_lines JSONB,
  p_reason TEXT,
  p_reference_journal_id UUID DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  journal_id UUID;
  adjustment_id UUID;
  line JSONB;
  total_debit NUMERIC := 0;
  total_credit NUMERIC := 0;
  account_id UUID;
  account_code TEXT;
  period_start_date DATE;
BEGIN
  -- Validate reason is not empty
  IF p_reason IS NULL OR TRIM(p_reason) = '' THEN
    RAISE EXCEPTION 'Adjustment reason is mandatory and cannot be empty';
  END IF;

  -- Validate period using assert_accounting_period_is_open
  -- Adjustments CANNOT be posted into locked periods
  PERFORM assert_accounting_period_is_open(p_business_id, p_adjustment_date);

  -- Resolve period_start from adjustment_date
  period_start_date := DATE_TRUNC('month', p_adjustment_date)::DATE;

  -- COA GUARD: Validate all account codes from p_lines before journal inserts
  FOR line IN SELECT * FROM jsonb_array_elements(p_lines)
  LOOP
    account_code := line->>'account_code';
    IF account_code IS NULL THEN
      RAISE EXCEPTION 'Account code is required for each adjustment line';
    END IF;
    PERFORM assert_account_exists(p_business_id, account_code);
  END LOOP;

  -- Validate that adjustments MUST balance (total debit = total credit)
  FOR line IN SELECT * FROM jsonb_array_elements(p_lines)
  LOOP
    total_debit := total_debit + COALESCE((line->>'debit_amount')::NUMERIC, 0);
    total_credit := total_credit + COALESCE((line->>'credit_amount')::NUMERIC, 0);
  END LOOP;

  IF ABS(total_debit - total_credit) > 0.01 THEN
    RAISE EXCEPTION 'Adjustment journal entry must balance. Debit: %, Credit: %', total_debit, total_credit;
  END IF;

  -- Create journal entry with reference_type = 'adjustment' and posting_source = 'accountant'
  INSERT INTO journal_entries (
    business_id,
    date,
    description,
    reference_type,
    reference_id,
    created_by,
    posting_source
  )
  VALUES (
    p_business_id,
    p_adjustment_date,
    'Adjustment: ' || p_reason,
    'adjustment',
    NULL, -- reference_id is NULL for adjustments (metadata stored in accounting_adjustments)
    auth.uid(), -- Use current user from auth context
    'accountant'  -- Manual adjustments are accountant postings
  )
  RETURNING id INTO journal_id;

  -- Create journal entry lines
  FOR line IN SELECT * FROM jsonb_array_elements(p_lines)
  LOOP
    account_code := line->>'account_code';
    
    IF account_code IS NULL THEN
      RAISE EXCEPTION 'Account code is required for each adjustment line';
    END IF;

    -- Get account ID by code
    account_id := get_account_by_code(p_business_id, account_code);

    IF account_id IS NULL THEN
      RAISE EXCEPTION 'Account with code % not found for business: %', account_code, p_business_id;
    END IF;

    INSERT INTO journal_entry_lines (
      journal_entry_id,
      account_id,
      debit,
      credit,
      description
    )
    VALUES (
      journal_id,
      account_id,
      COALESCE((line->>'debit_amount')::NUMERIC, 0),
      COALESCE((line->>'credit_amount')::NUMERIC, 0),
      line->>'description'
    );
  END LOOP;

  -- Store metadata row in accounting_adjustments
  INSERT INTO accounting_adjustments (
    business_id,
    adjustment_date,
    period_start,
    reason,
    reference_journal_id,
    created_by
  )
  VALUES (
    p_business_id,
    p_adjustment_date,
    period_start_date,
    p_reason,
    p_reference_journal_id,
    auth.uid()
  )
  RETURNING id INTO adjustment_id;

  RETURN journal_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

COMMENT ON FUNCTION post_adjustment_to_ledger IS 
  'Posts adjustment to ledger with posting_source = ''accountant''. Manual adjustments are accountant postings and require posted_by_accountant_id (set via created_by).';

-- ============================================================================
-- STEP 9: Update post_opening_balance_import_to_ledger() to set posting_source = 'accountant'
-- ============================================================================
-- Note: This function inserts directly into journal_entries, so we need to add posting_source
-- Read the function to find where it inserts
CREATE OR REPLACE FUNCTION post_opening_balance_import_to_ledger(
  p_import_id UUID,
  p_posted_by UUID
)
RETURNS UUID AS $$
DECLARE
  import_record RECORD;
  business_id_val UUID;
  period_id_val UUID;
  period_start_date DATE;
  period_end_date DATE;
  journal_entry_id_val UUID;
  existing_entry_id UUID;
  canonical_lines JSONB;
  input_hash_val TEXT;
  line JSONB;
  account_id_val UUID;
  account_code_val TEXT;
  debit_val NUMERIC;
  credit_val NUMERIC;
  memo_val TEXT;
BEGIN
  -- Get import record
  SELECT * INTO import_record
  FROM opening_balance_imports
  WHERE id = p_import_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Opening balance import not found: %', p_import_id;
  END IF;

  -- Validate import is in 'pending' status
  IF import_record.status != 'pending' THEN
    RAISE EXCEPTION 'Opening balance import must be in ''pending'' status. Current status: %', import_record.status;
  END IF;

  business_id_val := import_record.client_business_id;
  period_id_val := import_record.period_id;

  -- Get period dates
  SELECT period_start, period_end INTO period_start_date, period_end_date
  FROM accounting_periods
  WHERE id = period_id_val;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Accounting period not found: %', period_id_val;
  END IF;

  -- Convert lines to canonical format
  canonical_lines := COALESCE(import_record.canonical_lines, '[]'::jsonb);

  -- Compute input hash for idempotency
  IF import_record.input_hash IS NULL THEN
    input_hash_val := encode(
      digest(
        format(
          '%s|%s|%s|%s|%s|%s|%s|%s|%s',
          import_record.id,
          import_record.accounting_firm_id,
          import_record.client_business_id,
          import_record.period_id,
          import_record.source_type,
          canonical_lines::TEXT,
          ROUND(import_record.total_debit, 2)::TEXT,
          ROUND(import_record.total_credit, 2)::TEXT,
          COALESCE(import_record.approved_by::TEXT, '')
        ),
        'sha256'
      ),
      'hex'
    );
  ELSE
    input_hash_val := import_record.input_hash;
  END IF;

  -- ========================================================================
  -- STEP 9: IDEMPOTENCY CHECK - Check if ledger entry exists with same hash
  -- ========================================================================
  SELECT id INTO existing_entry_id
  FROM journal_entries
  WHERE source_type = 'opening_balance'
    AND input_hash = input_hash_val
  LIMIT 1;

  IF existing_entry_id IS NOT NULL THEN
    -- Link import to existing ledger entry
    UPDATE opening_balance_imports
    SET 
      journal_entry_id = existing_entry_id,
      posted_at = NOW(),
      posted_by = p_posted_by,
      status = 'posted',
      input_hash = input_hash_val
    WHERE id = p_import_id;

    RETURN existing_entry_id;
  END IF;

  -- ========================================================================
  -- STEP 10: CREATE LEDGER ENTRY + LINES (ATOMIC)
  -- ========================================================================
  -- Use period start date as the entry date for opening balances
  INSERT INTO journal_entries (
    business_id,
    date,
    description,
    reference_type,
    reference_id,
    created_by,
    source_type,
    source_import_id,
    input_hash,
    posting_source
  )
  VALUES (
    business_id_val,
    period_start_date,
    'Opening Balance Import',
    'opening_balance',
    NULL,
    p_posted_by,
    'opening_balance',
    p_import_id,
    input_hash_val,
    'accountant'  -- Opening balances are manual accountant postings
  )
  RETURNING id INTO journal_entry_id_val;

  -- Insert journal entry lines
  FOR line IN SELECT * FROM jsonb_array_elements(canonical_lines)
  LOOP
    account_code_val := line->>'account_code';
    debit_val := COALESCE((line->>'debit')::NUMERIC, 0);
    credit_val := COALESCE((line->>'credit')::NUMERIC, 0);
    memo_val := line->>'memo';

    -- Get account ID by code
    SELECT id INTO account_id_val
    FROM accounts
    WHERE business_id = business_id_val
      AND code = account_code_val
      AND deleted_at IS NULL;

    IF account_id_val IS NULL THEN
      RAISE EXCEPTION 'Account with code % not found for business: %', account_code_val, business_id_val;
    END IF;

    INSERT INTO journal_entry_lines (
      journal_entry_id,
      account_id,
      debit,
      credit,
      description
    )
    VALUES (
      journal_entry_id_val,
      account_id_val,
      debit_val,
      credit_val,
      COALESCE(memo_val, 'Opening balance')
    );
  END LOOP;

  -- Update import record
  UPDATE opening_balance_imports
  SET 
    journal_entry_id = journal_entry_id_val,
    posted_at = NOW(),
    posted_by = p_posted_by,
    status = 'posted',
    input_hash = input_hash_val
  WHERE id = p_import_id;

  RETURN journal_entry_id_val;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

COMMENT ON FUNCTION post_opening_balance_import_to_ledger IS 
  'Posts opening balance import to ledger with posting_source = ''accountant''. Opening balances are manual accountant postings.';
