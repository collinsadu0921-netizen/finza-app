CREATE OR REPLACE FUNCTION validate_accountant_posting(
  p_posted_by_accountant_id UUID,
  p_business_id UUID,
  p_posting_source TEXT
)
RETURNS BOOLEAN AS $$
DECLARE
  is_accountant BOOLEAN;
BEGIN
  -- For accountant postings, posted_by_accountant_id is REQUIRED
  IF p_posting_source = 'accountant' THEN
    IF p_posted_by_accountant_id IS NULL THEN
      RAISE EXCEPTION 'posted_by_accountant_id is required for accountant postings. Only accountants can post ledger entries manually.';
    END IF;
    
    -- Verify user is accountant for this business
    is_accountant := is_user_accountant(p_posted_by_accountant_id, p_business_id);
    
    IF NOT is_accountant THEN
      RAISE EXCEPTION 'User % does not have accountant role for business %. Only accountants can post ledger entries manually.', p_posted_by_accountant_id, p_business_id;
    END IF;
  END IF;
  
  -- For system postings, posted_by_accountant_id is NOT required
  -- System postings are trusted (generated by application logic)
  
  RETURN TRUE;
END;
$$ LANGUAGE plpgsql;

