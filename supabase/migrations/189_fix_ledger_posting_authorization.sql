-- without posted_by_accountant_id, while manual accountant postings remain
-- restricted.
--
-- Problem: Current guard requires posted_by_accountant_id for ALL postings,
-- blocking automatic retail postings which are system-generated.
--
-- Solution:
-- 1. Add posting_source column to distinguish system vs accountant postings
-- 2. Update authorization guard to only require posted_by_accountant_id for
--    accountant postings
-- 3. Update post_journal_entry() to accept posting_source parameter
-- 4. Update post_sale_to_ledger() to use posting_source = 'system'
